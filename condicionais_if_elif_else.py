# Condicionais em Python: if, elif, else
# Usados para controlar o fluxo do programa com base em condições.
# - if: executa um bloco de código se a condição for verdadeira.
# - elif: permite testar múltiplas condições, se o 'if' anterior for falso.
# - else: executa um bloco de código se todas as condições anteriores forem falsas.
# Exemplo:
# idade = 18
if idade < 18:
    print("Menor de idade")
elif idade == 18:
    print("Tem exatamente 18 anos")
else:
    print("Maior de idade")